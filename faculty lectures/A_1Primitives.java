public class A_1Primitives
/** 
 * Demo program for declaring primitives 
 */
{
    public static void main(String[]args)
    {
        int num; // declating variable num as tupe int
        num=3; // assigning the value 3 to num 

        double otherNum = 4.0; // walking and chewing gum at the same time

        double sum = num + otherNum;

        System.out.println("The sum of the numbers is: " + sum);
    
    }
}